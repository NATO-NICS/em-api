package edu.mit.ll.em.api.rs;

/**
 * Request object for adding or updating a Language Translation
 */
public class LanguageTranslationRequest {
    /**
     * Language code
     */
    private String code;

    /**
     * Language translation key
     */
    private String key;

    /**
     * Language translation value
     */
    private String value;

    /**
     * Language name
     */
    private String language;

    /**
     * Translated text for "Select an Organization for this session"
     */
    private String selectOrgText;

    public LanguageTranslationRequest() {
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getLanguage() {
        return language;
    }

    public void setLanguage(String language) {
        this.language = language;
    }

    public String getSelectOrgText() {
        return selectOrgText;
    }

    public void setSelectOrgText(String selectOrgText) {
        this.selectOrgText = selectOrgText;
    }
}
